//aritméticos

//valores literais
console.log(10 + 5); //soma
console.log(10 - 5); //subtração
console.log(10 * 5); //multiplicação
console.log(10 / 5); //divisão
console.log(10 % 5); //modulo - resto da divisão

//incremento e decremento
let x = 10;

console.log(x++); //acrescenta 1 mas não mostra logo no console
console.log(++x); //acrescenta 1 e mostra logo no console

console.log(x--); //subtrai 1 mas não mostra logo no console
console.log(--x); //subtrai 1 e mostra logo no console

//operadores relacionais
console.log(10 > 5); //maior
console.log(10 < 5); //menor
console.log(10 >= 5); //maior ou igual
console.log(10 <= 5); //menor ou igual
console.log(10 === 5); //mesmo valor e tipo
console.log(10 !== 5); //diferente valor ou tipo
